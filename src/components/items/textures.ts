// ===== 16x16 Pixel Art Item Textures =====
//
// Grid encoding:
//   '.' = transparent
//   '#' = base color
//   '1' = highlight (lighter)
//   '2' = shadow (darker)
//   '3' = accent color
//
// Each item has a 16x16 grid and a 4-color palette.

export interface ItemPalette {
    base: string;
    highlight: string;
    shadow: string;
    accent: string;
}

export interface ItemTextureData {
    grid: string[];
    palette: ItemPalette;
}

